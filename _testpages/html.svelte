<h5 class="dc">shorthand for elements</h5>
<code class="dc">
  {`element[.class]*[#id]
  *presses tab
<element class="class" id="id"></button>`}
</code>
<p class="dc">
  This is very useful for creating various elements with the `dc` class.
  Here an example with a button:
</p>
<div class="dc horizontal" style="margin-bottom: 0;">
  <code class="dc" style="width: 100%;">{`button.dc.bg.red  *presses tab*  <button class="dc bg red"></button>`}</code>
  <button class="dc bg red">I'm a red button</button>
</div>
<div class="dc divider" />

<h5 class="dc">List of stuff we have discord styling for</h5>
<p class="dc">To [<strong>button, code, p, h1, (h2, h3, h4,) h5, input, label</strong>] you can just add the class dc to get discord-esque styling</p>
